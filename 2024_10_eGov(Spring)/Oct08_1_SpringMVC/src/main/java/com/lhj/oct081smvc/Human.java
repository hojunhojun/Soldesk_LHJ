package com.lhj.oct081smvc;

public class Human {
	private String n;
	private int a;
	
	public Human() {
		
	}

	public Human(String n, int a) {
		super();
		this.n = n;
		this.a = a;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}
	
	
}
